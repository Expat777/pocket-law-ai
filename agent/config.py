# Детерминированный предохранитель роутинга: однозначная поверхностная форма в
# СЫРОМ вопросе -> спецзакон, даже если LLM-intent уронил его под шумом. Мотивация:
# на длинных бытовых вопросах intent цепляется за салиентную тему и теряет вторичный
# спецзакон (замер: «разделить квартиру В ИПОТЕКЕ, ...история про собаку...» —
# ипотека 0/5; сжатый тот же вопрос — 5/5). Тот же принцип, что fast-path по номеру
# статьи: детерминизм поверх LLM там, где поверхностный термин надёжен. Только
# ОДНОЗНАЧНЫЕ триггеры (не «залог» — он и про аренду): ключ = regex-фрагмент.
KEYWORD_TO_ACTS: dict[str, tuple[str, ...]] = {
    r"ипотек": ("Закон об ипотеке",),
    r"осаго": ("ОСАГО",),
    r"микрозайм|микрозаём|мфо\b": ("Закон о потребкредите",),
    r"материнск\w*\s+капитал|маткапитал": ("Закон о материнском капитале",),
    # «коллектор» — омоним (сантехнический коллектор/коллектор отопления). Триггерим
    # только людей: множественное число («коллекторы звонят») или «коллекторск(ое
    # агентство)». Одиночный «коллектор» отдаём LLM-intent: предохранитель обязан быть
    # ВЫСОКОТОЧНЫМ (его промах ловит intent, а ложный триггер + round-robin гарантируют
    # мусорной цитате место в ответе — хуже).
    r"коллектор(?:ы|ов|ам|ами|ах)\b|коллекторск": ("Закон о взыскании задолженности",),
}


def keyword_acts(text: str) -> list[str]:
    """Акты по однозначным ключевым словам в сыром вопросе (предохранитель роутинга).

    Возвращает в порядке словаря, без дублей. Пусто — предохранитель молчит (обычный
    случай). Сканируем СЫРОЙ вопрос, а не normalized: переформулировка могла убрать
    поверхностный термин («ипотека»), по которому и срабатывает предохранитель.
    """
    low = (text or "").lower()
    out: dict[str, None] = {}
    for pattern, acts in KEYWORD_TO_ACTS.items():
        if re.search(pattern, low):
            for a in acts:
                out.setdefault(a, None)
    return list(out)


def acts_for_branches(branches: list[str] | None) -> list[str]:
    """Отрасли -> список канонических актов (объединение, без дублей, порядок стабилен).

    Неизвестные/пустые отрасли просто игнорируются: пустой результат = «фильтра нет»,
    retrieve ищет по всем кодексам (защита от ошибки классификации intent).
    """
    seen: dict[str, None] = {}
    for b in branches or []:
        for act in BRANCH_TO_ACTS.get((b or "").strip().lower(), ()):
            seen.setdefault(act, None)
    return list(seen)