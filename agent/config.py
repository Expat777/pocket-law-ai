"""Настройки Роли 2 из окружения."""

import os

# Сколько фрагментов держим после retrieve. Роль 3 предупредила (STATUS.md):
# на «могут ли уволить в отпуске?» ст. 81 не попадает в топ-3 — берём запас и
# полагаемся на verify. Реальный потолок задаёт shared/search.py (TOP_K_LAW),
# поэтому это верхняя граница обрезки, а не гарантия K фрагментов.
TOP_K = int(os.getenv("AGENT_TOP_K", "10"))

# Минимальный score фрагмента закона, ниже которого считаем найденное мусором.
MIN_LAW_SCORE = float(os.getenv("AGENT_MIN_LAW_SCORE", "0.0"))

# Сколько цитат максимум показываем в ответе. search_law отдаёт до 10 статей —
# все в блок «Основание» вываливать не нужно (UX + ложное ощущение, что ответ
# опирается на все). В контекст LLM идут ВСЕ найденные, режем только цитаты.
MAX_CITATIONS = int(os.getenv("AGENT_MAX_CITATIONS", "5"))

# --- Защита бюджета LLM (см. также LLM_MAX_TOKENS в agent/llm/openai_compat.py) ---
# Потолок шагов графа за один ответ: граф линейный (≤2 вызова LLM), это страховка
# от зацикливания на случай будущих правок — прерываемся, а не жжём токены.
GRAPH_RECURSION_LIMIT = int(os.getenv("AGENT_RECURSION_LIMIT", "8"))

# Потолок длины вопроса: длиннее — обрезаем перед отправкой в LLM (input-токены).
MAX_QUESTION_CHARS = int(os.getenv("AGENT_MAX_QUESTION_CHARS", "4000"))

# --- Маршрутизация по кодексу (многокодексная база, Роль 3) ---
# intent называет отрасль(и) права -> сюда маппим в канонические имена актов, по
# которым фильтруется retrieve. Значения ОБЯЗАНЫ совпадать с полем `act` в payload
# Qdrant (Роль 3) — иначе фильтр молча не сматчит и уйдёт в фолбэк «искать по всем»
# (не сломается, но потеряем прирост точности). Сверить строки с Ролью 3 при
# добавлении каждого кодекса. Ключи — закрытый словарь отраслей для промпта intent.
BRANCH_TO_ACTS: dict[str, tuple[str, ...]] = {
    "трудовое": ("ТК РФ",),
    "уголовное": ("УК РФ",),
    "уголовный процесс": ("УПК РФ",),
    "гражданское": ("ГК РФ",),
    "гражданский процесс": ("ГПК РФ",),
    "семейное": ("СК РФ",),
    "административное": ("КоАП РФ",),
    "жилищное": ("ЖК РФ",),
    "налоговое": ("НК РФ",),
    "защита прав потребителей": ("ЗоЗПП",),
}

# Все известные акты (для валидации того, что вернул intent).
KNOWN_ACTS: frozenset[str] = frozenset(a for acts in BRANCH_TO_ACTS.values() for a in acts)


def acts_for_branches(branches: list[str] | None) -> list[str]:
    """Отрасли -> список канонических актов (объединение, без дублей, порядок стабилен).

    Неизвестные/пустые отрасли просто игнорируются: пустой результат = «фильтра нет»,
    retrieve ищет по всем кодексам (защита от ошибки классификации intent).
    """
    seen: dict[str, None] = {}
    for b in branches or []:
        for act in BRANCH_TO_ACTS.get((b or "").strip().lower(), ()):
            seen.setdefault(act, None)
    return list(seen)